Extending entirely across the state of Alabama for about {{convert|20|mi|km|0}} northern boundary, and in the middle stretching {{convert|60|mi|km|0}} farther south, is the [[Cumberland Plateau]], or [[Tennessee Valley]] region, broken into broad tablelands by the dissection of rivers. In the northern part of this plateau, west of [[Jackson County, Alabama|Jackson county]], there are about {{convert|1000|sqmi|km2}} of level highlands from {{convert|700|to|800|ft|m|-1}} above sea level. South of these highlands, occupying a narrow strip on each side of the [[Tennessee River]], is a country of gentle rolling lowlands varying in elevation from {{convert|500|to|800|ft|m|-1}}.  To the northeast of these highlands and lowlands is a rugged section with steep mountain-sides, deep narrow coves and valleys, and flat mountain-tops.  Its elevations range from {{convert|400|to|1800|ft|m|-1}}.  In the remainder of this region, the southern portion, the most prominent feature is ''''Little Mountain'''', extending about {{convert|80|mi|km|0}} from east to west between two valleys, and rising precipitously on the north side {{convert|500|ft|m|-1}} above them or {{convert|1000|ft|m}} above the sea.

Adjoining the Cumberland Plateau region on the southeast is the [[Ridge-and-valley Appalachians|Appalachian Valley]] (locally known as Coosa Valley) region, which is the southern extremity of the [[Appalachian Mountains]], and occupies an area within the state of about {{convert|8000|sqmi|km2|-3}}.  This is a [[limestone]] belt with parallel hard rock ridges left standing by erosion to form mountains.  Although the general direction of the mountains, ridges, and valleys is northeast and southwest, irregularity is one of the most prominent characteristics.  In the northeast are several flat-topped mountains, of which Raccoon and [[Lookout Mountain|Lookout]] are the most prominent, having a maximum elevation near the [[Georgia (U.S. state)|Georgia]] line of little more than {{convert|1800|ft|m|-1}} and gradually decreasing in height toward the southwest, where [[Sand Mountain (Alabama)|Sand Mountain]] is a continuation of Raccoon.  South of these the mountains are marked by steep northwest sides, sharp crests and gently sloping southeast sides.

Southeast of the Appalachian Valley region, the [[Piedmont (United States)|Piedmont Plateau]] also crosses the Alabama border from the N.E. and occupies a small triangular-shaped section of which [[Randolph County, Alabama|Randolph]] and [[Clay County, Alabama|Clay]] counties, together with the northern part of [[Tallapoosa County, Alabama|Tallapoosa]] and [[Chambers County, Alabama|Chambers]], form the principal portion.  Its surface is gently undulating and has an elevation of about {{convert|1000|ft|m|-2}} above sea level.  The Piedmont Plateau is a lowland worn down by erosion on hard crystalline rocks, then uplifted to form a plateau.

The remainder of the state is occupied by the ''''Coastal Plain''''.  This is crossed by foot-hills and rolling prairies in the central part of the state, where it has a mean elevation of about {{convert|600|ft|m|-1}}, becomes lower and more level toward the southwest, and in the extreme south is flat and but slightly elevated above the sea.
