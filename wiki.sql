An ''''''abscess'''''' ({{Lang-lat|abscessus}}) is a collection of [[pus]] ([[neutrophils]]) that has accumulated within a tissue because of an inflammatory process in response to either an [[infection|infectious]] process (usually caused by [[bacteria]] or [[parasite]]s) or other foreign materials (e.g., splinters, [[bullet]] [[wound]]s, or injecting needles). It is a [[immune system|defensive reaction]] of the tissue to prevent the spread of infectious materials to other parts of the body. 

The organisms or foreign materials kill the local [[cell (biology)|cells]], resulting in the release of [[cytokine]]s.  The cytokines trigger an [[inflammation|inflammatory response]], which draws large numbers of [[white blood cell]]s to the area and increases the regional [[blood]] flow.

The final structure of the abscess is an abscess wall, or capsule, that is formed by the adjacent healthy cells in an attempt to keep the pus from infecting neighboring structures. However, such encapsulation tends to prevent immune cells from attacking bacteria in the pus, or from reaching the causative organism or foreign object.

