An ''''''assembly language'''''' is a [[low-level programming language]] for a [[computer]], or other programmable device, in which there is a very strong (generally [[one-to-one correspondence|one-to-one]]) correspondence between the language and the [[computer architecture|architecture''s]] [[machine code]] [[instruction (computer science)|instructions]].  Each assembly language is specific to a particular computer architecture, in contrast to most [[high-level programming language]]s, which are generally [[porting|portable]] across multiple systems.

Assembly language is converted into executable machine code by a [[utility program]] referred to as an ''''[[#Assembler|assembler]]''''; the conversion process is referred to as ''''assembly'''', or ''''assembling'''' the code. 

Assembly language uses a [[mnemonic]] to represent each low-level machine operation or [[opcode]].  Some opcodes require one or more [[operand#Computer science|operands]] as part of the instruction, and most assemblers can take labels and symbols as operands to represent addresses and constants, instead of [[hard coding]] them into the program.  ''''''Macro assemblers'''''' include a [[macro (computer science)|macroinstruction]] facility so that assembly language text can be pre-assigned to a name, and that name can be used to insert the text into other code. Many assemblers offer additional mechanisms to facilitate program development, to control the assembly process, and to aid [[debugging]].

